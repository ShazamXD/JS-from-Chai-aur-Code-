/* LEXICAL SCOPE:
it simply means that inner function can ask from outer function but visa-versa is not possible also sibling will
not share anything means if two functions are defined under the same big parent function then they will not share
anything but each can get anything from parent function */


/* LEXICAL SCOPE:
Where a function is declared decides what variables it can access.
In other words:
Inner functions can see variables from outer functions, but not the other way around. */


/* CLOSURE:
A closure is formed when:
A function remembers variables from its outer scope even after the outer function has finished executing.
Closures happen because functions carry their scope(lexical scope) with them. */